/// Get the sample count for a color name.
pub fn get_sample_count(name: &str) -> u32 {
    CENTORE_SAMPLE_COUNTS
        .iter()
        .find(|(n, _)| n.eq_ignore_ascii_case(name))
        .map(|(_, count)| *count)
        .unwrap_or(0)
}

