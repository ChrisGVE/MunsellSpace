//! Centore polyhedron data for semantic color overlays.
//!
//! This module contains the exact convex hull vertex and face data for all 30 color names
//! from Paul Centore's 2020 paper "Beige, aqua, fuchsia, etc.: Definitions for some
//! non-basic surface colour names" (Journal of the International Colour Association, 25, 24-54).
//!
//! The data was extracted from PolyhedronFiles.zip, which contains the original convex hull
//! polyhedra derived from ~16,000 CAUS (Color Association of the United States) fabric samples.
//!
//! # Coordinate System
//!
//! Vertices are in Munsell Cartesian coordinates:
//! - x = chroma * cos(theta), where theta = hue_number * 9 degrees
//! - y = chroma * sin(theta)
//! - z = value (0-10)
//!
//! Faces are triangular, defined by vertex indices (0-based).


